function openNav() {
  document.getElementById("sideNav").style.width = "250px";
  document.getElementById("main" && "footer").style.marginLeft = "250px"; document.getElementById("main" && "footer").style.minWidth = "100%";
}

function closeNav() {
  document.getElementById("sideNav").style.width = "";
  document.getElementById("main" && "footer").style.marginLeft = "";
  document.getElementById("main" && "footer").style.minWidth = "";
}
